<?php
//função que soma 2 valores
function somar($v1,$v2){
    $soma = $v1+$v2;
    echo "Resultado da soma: {$soma}<br /><br />";
}

function subtrair($v1,$v2){
    $subtrair = $v1-$v2;
    echo "Resultado da subtração: {$subtrair}<br /><br />";
}

function multiplicar($v1,$v2){
    $multiplicar = $v1*$v2;
    echo "Resultado da multiplicação: {$multiplicar}<br /><br />";
}